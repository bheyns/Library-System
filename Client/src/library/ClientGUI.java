private void updateBookTable(ArrayList<String[]> books) {
        // Default data model is obtained
        DefaultTableModel dataModel = new DefaultTableModel();

        // Column names are assigned to the datamodel
        dataModel.addColumn("Book ISBN");
        dataModel.addColumn("Book Title");
        dataModel.addColumn("Book Type");
        dataModel.addColumn("Author");
        dataModel.addColumn("Publisher");
        // datamodel is assigned to search table
        displayTable.setModel(dataModel);

        if (books.isEmpty()) {
            JOptionPane.showMessageDialog(this, "There are no Books that match your search Criteria");
            // Else ensure message is changed back to default
        }
        // Add each new Movie row to the dataModel
        for (int i = 0; i < books.size(); i++) {
            dataModel.addRow(books.get(i));
        }
    }

