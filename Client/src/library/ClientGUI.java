    public void errorMessage(int flag) {
        switch (flag) {
            case 1:
                JOptionPane.showMessageDialog(this, "No Book/Member Matches");
                break;
            case 2:
                JOptionPane.showMessageDialog(this, "Cannot insert duplicate books");
                break;
            case 3:
                JOptionPane.showMessageDialog(this, "Successfully completed.");
                try {
                    searchAndUpdateTable("", "");
                } catch (Exception e) {
                    e.printStackTrace();
                }
                break;
            case 4:
                JOptionPane.showMessageDialog(this, "All displayed,Please Enter Values To Search.");
                break;
            case 5:
                   JOptionPane.showMessageDialog(this,"That publisher and/or Author does not exist");
                   break;
            case 6:
                JOptionPane.showMessageDialog(this,"Could not delete , please enter a valid ISBN number or Member ID to delete Book or Member");
                break;
            case 7:
                JOptionPane.showMessageDialog(this,"Successfully borrowed the book");
                break;    
            
            default:
                JOptionPane.showMessageDialog(null, "Error");
        }
    }
    
